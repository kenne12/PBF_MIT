package utils;

/**
 *
 * @author USER
 */
public class SmsSubResponse {

    private String number;
    private String id;
    private String status;

    public SmsSubResponse() {
    }

    public SmsSubResponse(String number, String id, String status) {
        this.number = number;
        this.id = id;
        this.status = status;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

}
